      exercises.where((ex) => ex['equipment'].toString().contains(equipment)).toList();

  /// Returns a single exercise by ID
  static Map<String, dynamic>? getExerciseById(String id) {
    try {
      return exercises.firstWhere((ex) => ex['id'] == id);
    } catch (_) {
      return null;
    }
  }

  /// Returns a single exercise by English name (case-insensitive)
  static Map<String, dynamic>? getExerciseByName(String name) {
    try {
      return exercises.firstWhere(
        (ex) => ex['name'].toString().toLowerCase() == name.toLowerCase(),
      );
    } catch (_) {
      return null;
    }
  }

  /// Returns a formatted exercise list for Gemini AI prompts
  static String getExerciseListForPrompt() {
    final buffer = StringBuffer();
    buffer.writeln('AVAILABLE EXERCISES (use ONLY from this list):');
    buffer.writeln();
    final grouped = <String, List<Map<String, dynamic>>>{};
    for (final ex in exercises) {
      grouped.putIfAbsent(ex['bodyPart'] as String, () => []).add(ex);
    }
    grouped.forEach((part, list) {
      buffer.writeln('=== $part ===');
      for (final ex in list) {
        buffer.writeln('- ${ex['name']} (${ex['difficulty']}, ${ex['equipment']})');
      }
      buffer.writeln();
    });
    return buffer.toString();
  }
